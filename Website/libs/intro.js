var smokeImage = new Image();
    smokeImage.src="imgs/smokeParticle.png";
var waterfall            = new Ion(250);
waterfall.sx         = function(){return r(0,v.w);};
waterfall.sy         = function(){return r(0,v.h/4)+ v.h/4;};
waterfall.dx         = function(){return v.w;};
waterfall.dy         = function(){return r(0,v.h/4)+ v.h/4;};
waterfall.wx         = function(){return r(0,3)-1.5;};
waterfall.wy         = function(){return r(0,1)-0.5;};
waterfall.size       = function(){return r(1,3);};
waterfall.tween_type = 7;
waterfall.tween_speed= 40;
waterfall.image      = smokeImage;
waterfall.imageWidth = 256;
waterfall.imageHeight= 256;
waterfall.onEscape   = function(id){this.onEnd(id);};
waterfall.onEnd      = function(id){
  this.particle[id].x  = this.particle[id].sx=r(0,v.w);
  this.particle[id].y  = this.particle[id].sy=r(0,v.h/4)+ v.h/4;
  //this.particle[id].dx = v.w;
  //this.particle[id].dy = r(v.h/4)+ v.h/4;
  this.particle[id].c  = 0;
};
waterfall.populate();
waterfall.process();