    private void computePerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_computePerformed
       double loanAmt=Double.parseDouble(txtLoanAmount.getText());
        double interestRate=Double.parseDouble(txtAnnualInterestRate.getText());
        int years=Integer.parseInt(txtNumberOfYears.getText());
        Loan theLoan = new Loan(interestRate,years,loanAmt);
        txtResult.setText("Loan Amount: "+loanAmt+"\n"+
                          "Annual Interest Rate: "+interestRate+"\n"+
                          "Number of Years: "+years+"\n"+
                          "Monthly Payment: "+theLoan.getMonthlyPayment()+"\n"+
                          "Total Payment: "+theLoan.getTotalPayment());
    }//GEN-LAST:event_computePerformed
    
